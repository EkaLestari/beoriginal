    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        LogUsuarioBL objLogUsuarioBL = new LogUsuarioBL();
        if (!(this.txtUsuario.getText().equals("")) && !(this.txtPassword.getText().equals(""))) {
            try{
                intentos++; // Cantidad de intentos para autenticarse

                String strContrasenhaEncriptada = objEncriptador.encriptar(this.txtPassword.getText());

                AccesoSistemaBL Logicaacceso = new AccesoSistemaBL();
                AccesoSistemaBE objAcceso = new AccesoSistemaBE(txtUsuario.getText(),strContrasenhaEncriptada);
                UsuarioBE objUsuario = new UsuarioBE();
                objUsuario= Logicaacceso.validarCuenta(objAcceso);
                if (objUsuario !=  null){
                    LogUsuarioBE objLogUsuarioBE = new LogUsuarioBE();
                    objLogUsuarioBE.setObjUsuarioBE(objUsuario);

                    boolean registro = objLogUsuarioBL.registrarLog(objLogUsuarioBE);
                    int codlog=objLogUsuarioBL.obtenerIdLog();
                    if (registro){
                       //if(objAcceso.getStrClave().equals(objEncriptador.encriptar(objUsuario.getPassword()))){
                       //if (objAcceso.getStrClave().equals(objUsuario.getPassword())){
                        if(objAcceso.getStrClave().equals(objUsuario.getPassword()) || objUsuario.getPassword().equals("pass")){
                            frmVentanaPrincipal ventana = new frmVentanaPrincipal(objUsuario,codlog);
                            conectado=true;
                            this.setVisible(false);
                            //deshabilita todos los menus
                            setEnableMenus(ventana,false);
                            //habilitar menus permitidos al usario
                            ModuloBL objModuloBL = new ModuloBL();
                            ArrayList<ModuloBE> listaModulo = new ArrayList<ModuloBE>();
                            listaModulo = objModuloBL.getModulo(objUsuario.getCodPerfil());

                            if (listaModulo != null){
                                for (int cont=0;cont<listaModulo.size();cont++){
                                    //System.out.printf("%s\n",listaFuncionalidad.get(cont).getStrNameVentana());
                                    setMenuItem( ventana,listaModulo.get(cont).getModulo());
                                }
                            } else{System.out.printf("No listo las funcionalidadess");}
                            this.txtPassword.setText("");
                            this.txtUsuario.setText("");

                            //muestro la pantalla Principal
                            ventana.setVisible(true);

                        }
                        else{
                            // Falta tunear para saber qe fallo, si el usuario o la contraseña
                            JOptionPane.showMessageDialog(null,"Error en ingreso de datos."," Mensaje ",JOptionPane.ERROR_MESSAGE);
                            this.txtUsuario.setText("");
                            this.txtPassword.setText("");
                            this.txtUsuario.requestFocus();
                        }
                    } else{
                        //verifica el numero de intentos
                        if (intentos == 3 &&  conectado==false){
                            JOptionPane.showMessageDialog(null,"Se excedió el número de intentos máximo"," Mensaje ",JOptionPane.ERROR_MESSAGE);
                            this.dispose();
                            System.exit(0);
                        } else{
                            JOptionPane.showMessageDialog(null,"Nombre o Clave incorrecta"," Mensaje ",JOptionPane.ERROR_MESSAGE);
                            this.txtPassword.setText("");
                            this.txtUsuario.transferFocus();
                        }
                    }
                }

            } catch(Exception exc){
                System.out.printf("ERROR=%s",exc.getMessage());
                JOptionPane.showMessageDialog(null,"Error en Ingreso","Mensaje ",JOptionPane.ERROR_MESSAGE);
            }
        } else{
            JOptionPane.showMessageDialog(null,"Debe Ingresar el nombre del Usuario y la Clave"," Mensaje ",JOptionPane.ERROR_MESSAGE);
            this.btnSalir.transferFocus();
        }

    }//GEN-LAST:event_btnIngresarActionPerformed

    private void btnRestablecerContrasenaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRestablecerContrasenaActionPerformed
        // TODO add your handling code here:
        UsuarioDA objUsuarioDA=new UsuarioDA();
        usr = txtUsuario.getText().toString();
        if (txtUsuario.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un nombre de usuario","ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        correo_destino = objUsuarioDA.ObtenerCorreo(usr);
        nueva_contrasenha=generadorContrasenha.getPassword(generadorContrasenha.NUMEROS+generadorContrasenha.MINUSCULAS+generadorContrasenha.MAYUSCULAS+generadorContrasenha.ESPECIALES,(9+5)/2);
        try{
            EnvioContrasenha.enviarCorreo(correo_destino, nueva_contrasenha);
        } catch(Exception ex){
            System.out.println("No se pudo modificar la contraseña");
        }
        byte[] byteContrasenhaEncriptada= objEncriptador.getHash(nueva_contrasenha);
        String strContrasenhaEncriptada ="";
        for (int i=0;i<byteContrasenhaEncriptada.length;i++){
            strContrasenhaEncriptada +=byteContrasenhaEncriptada[i];
        }
        objUsuarioDA.ActualizarContrasena(usr,strContrasenhaEncriptada);

    }//GEN-LAST:event_btnRestablecerContrasenaActionPerformed

    
