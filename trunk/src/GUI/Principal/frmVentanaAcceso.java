    private void setEnableMenus(frmVentanaPrincipal form,boolean estado) {
        int cont_menus=0;
        while(cont_menus<form.getJMenuBar().getMenuCount()-1){
            int cont_items=0;
          form.getJMenuBar().getMenu(cont_menus).setEnabled(false);
          cont_menus++;
        }
    }

    // Habilita los menus y submenus segun el perfil del usuario
    private void setMenuItem(frmVentanaPrincipal formulario,String nombre) {

           if (nombre.equals("Documentos"))
                formulario.mnuDocumentos.setEnabled(true);
           else
               if (nombre.equals("Evaluacion"))
                    formulario.mnuEvaluacion.setEnabled(true);
               else
                    if (nombre.equals("Seguridad"))
                        formulario.mnuSeguridad.setEnabled(true);
                    else
                        if (nombre.equals("Auditoria"))
                            formulario.mnuAuditoria.setEnabled(true);
    }

